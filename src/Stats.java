
public class Stats {
    private int length;
    private int countA;
    private int countC;
    private int countT;
    private int countG;

    public int getLength(){
        return length;
    }
    public void setLength(int length){
        this.length = length;
    }
    public int getCountA() {
        return countA;
    }
    public void setCountA(int countA) {
        this.countA = countA;
    }
    public int getCountC() {
        return countC;
    }
    public void setCountC(int countC) {
        this.countC = countC;
    }
    public int getCountT() {
        return countT;
    }
    public void setCountT(int countT) {
        this.countT = countT;
    }
    public int getCountG() {
        return countG;
    }
    public void setCountG(int countG) {
        this.countG = countG;
    }
}
